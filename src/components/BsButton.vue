<script>
import { defineComponent, h } from 'vue';

const Spinner = defineComponent({
  name: 'Spinner',
  render() {
    return h('span', { class: 'spinner' }, [
      h('span'),
      h('span'),
      h('span'),
      h('span')
    ]);
  },
});

const variants = [
  'none', 'primary', 'secondary', 'success', 'danger', 'warning', 'info', 'light', 'dark', 'link', 
  'outline-primary', 'outline-secondary', 'outline-success', 'outline-danger', 'outline-warning', 
  'outline-info', 'outline-light', 'outline-dark'
];

export default {
  name: 'bs-button',
  components: {
    Spinner
  },
  props: {
    disabled: {
      type: Boolean,
      default: false
    },
    loading: {
      type: Boolean,
      required: false,
      default: false
    },
    small: {
      type: Boolean,
      default: false
    },
    variant: {
      type: String,
      default: 'none',
      validator: v => !!~variants.indexOf(v),
    },
    type: {
      type: String,
      default: 'button'
    },
    href: {
      type: String
    }
  },
  render() {
    const variantClass = this.variant == 'none' ? '' : `btn-${this.variant}`;
    const attrs = { class: `btn ${variantClass}`, type: this.type };
    const children = [ this.$slots.default({}) ];

    if (this.small) {
      attrs.class += ' btn-sm';
    }

    if (this.loading) {
      children.push(h(Spinner));
      attrs.class += ' btn-loading';
    }

    if (this.disabled || this.loading) {
      attrs.disabled = true;
    }
    
    if (this.href) {
      attrs.href = this.href;
      attrs.role = 'button';
    }

    return h(this.href ? 'a' : 'button', attrs, children);
  },
}
</script>